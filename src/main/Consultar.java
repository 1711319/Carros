    private void jCBCarrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCBCarrosActionPerformed
        // TODO add your handling code here:
        // Cada iterração com a lista implica que os dados da seleção(index)
        // proporcione o carregamento dois dados de cada carro;

        for (int i = 0; i < tamanhoOpcoes; i++) {
            if (jCBCarros.getSelectedIndex() == i) {
                jLblConsMarca.setText("Marca: " + opcoesSeparadas[i][0]);
                jLblConsModelo.setText("Modelo: " + opcoesSeparadas[i][1]);
                jLblConsAno.setText("Ano: " + opcoesSeparadas[i][5]);
                jLblConsCor.setText("Cor: " + opcoesSeparadas[i][2]);
                jLblConsVelMax.setText("Vel. Max.: " + itemString[3]);
            }
        }


    }//GEN-LAST:event_jCBCarrosActionPerformed

