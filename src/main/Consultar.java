    Carro_Class classCarro = new Carro_Class();
    List<String[]> dados = Carro_Class.lerDados("dados.txt");

    String[] opcoes = new String[dados.size()];
    String[][] opcoesSeparadas = new String[opcoes.length][6];

    String[] itemString = new String[6];
    int tamanhoOpcoes = 0;
    // String[] opcoes = {};

    public Consultar() {
        pack(); // Ajusta o tamanho do JFrame com base nos componentes
        setLocationRelativeTo(null); // Centraliza o formulário na tela
        initComponents();
        jCBCarros.removeAllItems(); // Limpa todos os itens da comboBox

        tamanhoOpcoes = opcoes.length; // Variável armazena o tamanho do array opcoes 

        // O dados recebidos devem ser tratados e armazenados.
        for (int i = 0; i < dados.size(); i++) {
            opcoes[i] = dados.get(i)[0];
            itemString = opcoes[i].split(";");
            opcoesSeparadas[i][0] = itemString[0];  // Marca
            opcoesSeparadas[i][1] = itemString[1];  // Modelo
            opcoesSeparadas[i][2] = itemString[2];  // Cor 
            opcoesSeparadas[i][3] = itemString[3];  // VelMax
            opcoesSeparadas[i][4] = itemString[4];  // VelMax
            opcoesSeparadas[i][5] = itemString[5];  // Ano

        }

        for (int i = 0; i < tamanhoOpcoes; i++) {
            jCBCarros.addItem(opcoesSeparadas[i][0] + " , " + opcoesSeparadas[i][1]);

        }

    }

